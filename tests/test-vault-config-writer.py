def test_end_to_end_file_roundtrip():
    """Run cmd_tags against a real file → reparse → match input."""
    with tempfile.TemporaryDirectory() as td:
        td_path = Path(td)
        cfg_path = td_path / "vault-config.yaml"
        cfg_path.write_text(SAMPLE_CONFIG_WITH_TAGS, encoding="utf-8")
        input_path = td_path / "tags.json"
        input_path.write_text(json.dumps(VALID_INPUT), encoding="utf-8")

        class Args:
            input = str(input_path)
            config = str(cfg_path)
            stdout = False

        rc = vcw.cmd_tags(Args())
        _must(rc == 0, f"cmd_tags returned non-zero: {rc}")

        reparsed = yaml.safe_load(cfg_path.read_text(encoding="utf-8"))
        _must(isinstance(reparsed["tags"]["prefixes"], dict),
              "post-write YAML must have tags.prefixes as dict")
        _must(set(reparsed["tags"]["prefixes"]) == set(VALID_INPUT["prefixes"]),
              "post-write prefix set must match input")
        # Also confirm unrelated sections survived
        _must(reparsed["profile"] == "miyo", "profile preserved")
        _must(reparsed["lifecycle"]["tag_prefix"] == "MiYo-Tomo", "lifecycle preserved")
    print("[PASS] end-to-end: file written, reparsed, unrelated sections preserved")


