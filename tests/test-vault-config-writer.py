def _must(cond: bool, msg: str) -> None:
    if not cond:
        print(f"FAIL: {msg}", file=sys.stderr)
        sys.exit(1)


def _expect_fail(fn, *args, label: str = "") -> None:
    """Call fn(*args) and assert it calls sys.exit(1) (validation failure)."""
    try:
        fn(*args)
    except SystemExit as e:
        _must(e.code == 1, f"{label}: expected exit 1, got {e.code}")
        return
    _must(False, f"{label}: expected validation failure, got success")


