def test_render_format_contract():
    out = vcw.render_tags_section(VALID_INPUT)
    # Top structure
    _must(out.startswith("tags:\n  prefixes:\n"), "render must start with tags: / prefixes:")
    _must(out.endswith("\n"), "render must end with a newline")
    # Per-entry fields
    for name in VALID_INPUT["prefixes"]:
        _must(f"    {name}:" in out, f"missing prefix key line for {name}")
    # Double-quoted strings
    _must('"Note type (structural)"' in out, "description must be double-quoted")
    _must('"note/normal"' in out, "known_values must be double-quoted")
    # Empty lists inline
    _must("known_values: []" in out, "empty known_values should be inline []")
    _must("required_for: []" in out, "empty required_for should be inline []")
    # Booleans lowercase
    _must("wildcard: false" in out, "wildcard false should be lowercase")
    _must("wildcard: true" in out, "wildcard true should be lowercase")
    _must("proposable: false" in out, "proposable false should be lowercase")
    _must("proposable: true" in out, "proposable true should be lowercase")
    # required_for entries as bare tokens (no quotes)
    _must("        - atomic_note" in out, "required_for items should be bare tokens")
    print("[PASS] render format matches vault-example.yaml conventions")


def test_render_is_valid_yaml():
    out = vcw.render_tags_section(VALID_INPUT)
    parsed = yaml.safe_load(out)
    _must("tags" in parsed and "prefixes" in parsed["tags"],
          "rendered YAML must parse with tags.prefixes")
    prefixes = parsed["tags"]["prefixes"]
    _must(isinstance(prefixes, dict), "parsed prefixes must be dict")
    _must(set(prefixes) == set(VALID_INPUT["prefixes"]),
          f"prefix keys mismatch: {set(prefixes)} vs {set(VALID_INPUT['prefixes'])}")
    for name, entry in VALID_INPUT["prefixes"].items():
        got = prefixes[name]
        _must(got["wildcard"] == entry["wildcard"], f"{name}: wildcard mismatch")
        _must(got["proposable"] == entry["proposable"], f"{name}: proposable mismatch")
        _must(got["known_values"] == entry["known_values"], f"{name}: known_values mismatch")
        _must(got["required_for"] == entry["required_for"], f"{name}: required_for mismatch")
        _must(got["description"] == entry["description"], f"{name}: description mismatch")
    print("[PASS] rendered YAML parses back to the input dict")


